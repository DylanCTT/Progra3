package TP1;

public class ClienteEj9 {
	private String nombre;
	private Boolean esEmbarazada;
	
	public ClienteEj9(){
		
	}

	public ClienteEj9(String nombre, Boolean esEmbarazada) {
		this.nombre = nombre;
		this.esEmbarazada = esEmbarazada;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public Boolean getEsEmbarazada() {
		return esEmbarazada;
	}

	public void setEsEmbarazada(Boolean esEmbarazada) {
		this.esEmbarazada = esEmbarazada;
	}
	
	
	
	
}
