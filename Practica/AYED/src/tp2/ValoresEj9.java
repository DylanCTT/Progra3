package tp2;

public class ValoresEj9 {

	private int sum;
	private int dif;
	
	public ValoresEj9() {
		
	}
	
	public ValoresEj9(int sum, int dif) {
		this.sum = sum;
		this.dif = dif;
	}
	
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getDif() {
		return dif;
	}
	public void setDif(int dif) {
		this.dif = dif;
	}

	@Override
	public String toString() {
		return "ValoresEj9 [sum=" + sum + ", dif=" + dif + "]";
	}
	
	
	
}
